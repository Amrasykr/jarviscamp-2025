/**
 * ========================
 * FOREACH LOOP DI PHP
 * ========================
 *
 * Foreach digunakan untuk melakukan perulangan pada elemen-elemen dalam array.
 * Cocok saat ingin membaca semua item dalam array, tanpa peduli jumlahnya berapa.
 */

// Contoh: Menampilkan daftar buah
$buah = ["Apel", "Jeruk", "Mangga"];

foreach ($buah as $item) {
    echo "Buah: $item\n"; // menampilkan setiap buah
}

